<h2><font face="Geneva, Arial, Helvetica, sans-serif">Speed &amp; Bandwidth</font></h2>
<p><font size="3" face="Geneva, Arial, Helvetica, sans-serif">If chat takes an 
  excessively long time to display your chat message, then it could be an indication 
  of server lockup, or a slow web server. In these cases, the site administrator 
  simply needs to move chat to a faster computer, or put it on a dedicated server. 
  Most chat rooms consume a very large amount of server resources, because they 
  must constantly check the server for updates to the chat room.</font></p>
<p><font size="3" face="Geneva, Arial, Helvetica, sans-serif">Low end-user bandwidth 
  is another common culprit. While chat has been sucessfully tested on connection 
  speeds as low as 28.8 kbps, it works best on high-speed connections. It is strongly 
  recommended that you use a connection of at least 128 kbps during the chat.</font></p>
<h2><font face="Geneva, Arial, Helvetica, sans-serif">Room Management</font></h2>
<p><font size="3" face="Geneva, Arial, Helvetica, sans-serif">There are infinitely 
  many chat rooms possible. If permitted by the site administrator, end-users 
  may create as many rooms as they wish, specify the maximum occupancy of the 
  new rooms, and make the rooms private or invite specific users to join them 
  in a public or private room. This option is not available when operating Chat 
  in so-called Tech Support mode.</font></p>
<h2><font face="Geneva, Arial, Helvetica, sans-serif">Sending a Message</font></h2>
<p><font size="3" face="Geneva, Arial, Helvetica, sans-serif">To send a message, enter
  your text in the rectangle at the bottom, labeled &quot;Message:&quot;. Then
  press &lt;Enter&gt; or click on the Submit button. The Submit button will become
  inactive, indicating the you must wait for a response from the server before
  sending another message. This slows down chat operation slightly, but it helps
  to ensure that the server doesn't get bogged down with too many submit requests
  at once.</font></p>
<h2><font face="Geneva, Arial, Helvetica, sans-serif">Colors &amp; Sounds</font></h2>
      <p><font size="3" face="Geneva, Arial, Helvetica, sans-serif">Depending on how 
        the site administrator has set up the chat software, you may or may not 
        be able to edit the display and sound options. To change the display, 
        just choose the part of the chat interface that you would like to change, 
        and then click on the appropriate color. Or, use one of the 'skins' to 
        give the chat room one of several pre-defined looks.</font></p>
<h2><font face="Geneva, Arial, Helvetica, sans-serif">Private Messages</font></h2>
<p><font size="3" face="Geneva, Arial, Helvetica, sans-serif">Private Messages can be sent
  by specifying the 'send a private message' option in the drop menu, and then
  choosing the recipient of the message in the adjacent menu. The actual text
  of the private message is then typed into the <em>same </em> area that you type
  your regular messages. Depending on how the site administrator has set up the
  chat software, you may be able to send private messages to any user in any room,
  or just to users in the room you are in. </font></p>
<p><font size="3" face="Geneva, Arial, Helvetica, sans-serif">To invite other 
  users into a private room that you or another user have created, simply use 
  the drop-menu at the top of the chat room, and specify the user that you would 
  like to invite into your chat with you. The private room will be added to this 
  user's list of available rooms, and he or she will be notified of this addition. 
  The user must still select the private room from the list to actually join in.</font></p>
