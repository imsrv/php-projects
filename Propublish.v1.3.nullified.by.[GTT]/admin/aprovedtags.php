In text-mode, there are some special tags you can use. Some of them (like image-tag) can also be used in WYSIWYG (graphics) mode.
Note that you often can use both [ and normal html tags.<p>
<u>Examples:</u><p>
<table width=100% border=0 cellspacing="1" cellpadding="3">
<tr><td><b>TAG</b><br>&nbsp;</td><td><b>RESULT</b><br>&nbsp;</td></tr>
<tr><td>&ltcode&gt;&lt? print "bla"; ?&gt;&lt/code&gt;</td><td>(Shows code in a table)<br></td></tr>
<tr><td>&ltimage&gt;23&lt/image&gt</td><td>(Shows uploaded image from db)<br></td></tr>
<tr><td>&ltimage_left&gt;23&lt/image&gt</td><td>(Shows uploaded image from db)<br></td></tr>
<tr><td>&ltimage_right&gt;23&lt/image&gt</td><td>(Shows uploaded image from db)<br></td></tr>
<tr><td>[b]Bold type[/b]</td><td><b>Bold type</b><br></td></tr>
<tr><td>[url]http://www.sol.no[/url]</td><td><a href="http://www.sol.no">Link</a><br></td>
<tr><td>[i]This is ingress type[/i]</td><td><i>This is ingress type</i><br></td></tr>
<tr><td>[u]Underlined[/u]</td><td><u>Underlined</u><br></td></tr>
<tr><td>[img]http://banner.gif[/img]</td><td>(Shows image)<br></td></tr>
<tr><td>[h1]Largest headline[/h1]</td><td>(Largest headline)<br></td></tr>
<tr><td>[h2]Smaller[/h2]</td><td>(Smaller)<br></td></tr>
<tr><td>[h3]Smaller[/h3]</td><td>(Smaller)<br></td></tr>
<tr><td>[h4]Smaller[/h4]</td><td>(Smaller)<br></td></tr>
<tr><td>[h5]Smallest[/h5]</td><td>(Smallest)<br></td></tr>
<tr><td>[bq]Blabla[/bq]</td><td>(Blockquote)<br></td></tr>
<tr><td>[ol]Ordered list[/ol]</td><td>(Ordered list)<br></td></tr>
<tr><td>[ul]Unordered list[/ul]</td><td>(Unordered list)<br></td></tr>
<tr><td>[li]List-element[/li]</td><td>(List-element)<br></td></tr>
</table>
